# Randomly choose a name
guess = random.choice(names)
len_guess = len(guess)
print(f"THE random name has {len_guess} letters")
print("WARNING!!! DONT LET THE PATTERN TO COMPLETE")

# Initialize display
display = ["_" for _ in range(len_guess)]

# Game loop
loop = False
lives = 6
while not loop:
    user_input = input("Enter your guess (single letter): ").lower()
    # Check if the letter was already guessed
    if user_input in display:
        print("You already guessed this letter.")
        continue
    
    # Input validation
    if len(user_input) != 1 or not user_input.isalpha():
        print("Please enter a single letter.")
        continue

    # Update the display list with correct guesses
    if user_input in guess:
        for i in range(len_guess):
            if guess[i] == user_input:
                display[i] = user_input
    else:
        # If the guessed letter is not in the word, reduce lives
        lives -= 1
        print(f"Entered letter is not in the name. Lives remaining: {lives}")

    # Print current status and the corresponding hangman image
    print(f"Status of your guess: {display}")
    print(images[lives])

    # Check win condition
    if "_" not in display:
        print("HURRAY YOU WON ......")
        loop = True
    
    # Check lose condition
    if lives == 0:
        print("YOU LOST THE GAME..BETTER LUCK NEXT TIME")
        print(f"The word was: {guess}")
        loop = True
